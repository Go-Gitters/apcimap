	<p>propertyClass (R or C for Residential or commercial)</p>
	<p>propertySitusCity (number and street for location of property)</p>
	<p>propertySitusStreetAddress (city & sometimes state & zip)</p>
	<p>propertyValue (Assessed property value) </p>
	<br>
	<p>propertyLocation (Latitude & Longitude)  <-- This is not included in the database, but can be generated from the .SHP files from the assessor.  (Kyla will do that as part of the ticket to bring data into database.)</p>

